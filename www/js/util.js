/**
 * 
 * globalConfigureHighCharts() do any common/global configuration of
 * the High Charts package, in particular make sure timezone is set 
 * correctly.
 * 
 */

function globalConfigureHighCharts() {

  /* 
   * We need to adjust Highcharts for Daylight Savings Time and our 
   * timezone at the sametime...it doesn't know about our location
   * or if DST/EST is in play.  So we have to give it an offset 
   * that incorporates both.
   * 
   */

  var tzOffset = 5 * 60;
  if(moment().isDST()) {
    tzOffset = 4 * 60;
  }

  console.log(". timeone offset is: " + tzOffset);

  Highcharts.setOptions({
    global: {
      timezoneOffset: tzOffset
    }
  });

}
